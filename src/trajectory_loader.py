"""
Trajectory Loader for Pseudo-RL Training
Loads generated trajectories and formats them for training with attempt sections
"""

import torch
from pathlib import Path
from typing import Dict, List, Optional, Tuple, Any
import logging
from datetime import datetime

from src.token import SpecialToken
from src.utils.grid_data_process import create_special_token_sequence


class TrajectoryLoader:
    """
    Loads trajectory files generated by TrajectoryGenerator and formats them
    for training with ATTEMPT_START/END sections
    """
    
    def __init__(self, trajectory_folder: str):
        """
        Initialize trajectory loader
        
        Args:
            trajectory_folder: Path to folder containing trajectory .pt files
        """
        self.trajectory_folder = Path(trajectory_folder)
        if not self.trajectory_folder.exists():
            raise ValueError(f"Trajectory folder does not exist: {trajectory_folder}")
        
        self.trajectories = {}
        self.ground_truths = {}
        
        logging.info(f"TrajectoryLoader initialized with folder: {trajectory_folder}")
    
    def load_trajectories(self) -> Dict[int, Dict[str, Any]]:
        """
        Load all trajectory files from the folder
        Accumulates trajectories from multiple iterations with unique indices
        
        Returns:
            Dictionary mapping sample indices to trajectory data
        """
        trajectory_files = sorted(self.trajectory_folder.glob("trajectories_*.pt"))
        
        if not trajectory_files:
            logging.warning(f"No trajectory files found in {self.trajectory_folder}")
            return {}
        
        all_trajectories = {}
        global_index_offset = 0  # Track global index across files
        
        for file_path in trajectory_files:
            logging.info(f"Loading trajectory file: {file_path}")
            
            try:
                data = torch.load(file_path, map_location='cpu')
                
                # Check if it's the new format (list of trajectory dicts) or old format
                if isinstance(data, list):
                    # New format from updated trajectory generator
                    for local_idx, traj in enumerate(data):
                        # Use global index to ensure uniqueness across files
                        global_idx = global_index_offset + local_idx
                        
                        # Extract the generated output as attempt tokens
                        generated = traj.get('generated_output', [])
                        attempt_tokens = self._format_as_attempt_tokens(generated)
                        
                        # Store in dictionary with unique global index as key
                        all_trajectories[global_idx] = {
                            'attempt_tokens': attempt_tokens,
                            'input_sequence': traj.get('input_sequence', []),
                            'ground_truth': traj.get('ground_truth_output', None),
                            'is_trajectory': True,  # Mark as trajectory data
                            'source_file': file_path.name  # Track source for debugging
                        }
                    
                    logging.info(f"Loaded {len(data)} trajectories from {file_path} (new format)")
                    global_index_offset += len(data)
                    
                elif isinstance(data, dict):
                    # Old format from InferenceOutputSaver
                    generated_sequences = data.get('generated', [])
                    input_sequences = data.get('inputs', [])
                    metadata = data.get('metadata', {})
                    ground_truth_outputs = metadata.get('ground_truth_outputs', [])
                    
                    # Process each trajectory
                    for local_idx, (generated, input_seq) in enumerate(zip(generated_sequences, input_sequences)):
                        # Use global index to ensure uniqueness across files
                        global_idx = global_index_offset + local_idx
                        
                        # The generated sequence is just the output portion
                        # We need to format it as attempt tokens
                        attempt_tokens = self._format_as_attempt_tokens(generated)
                        
                        # Store in dictionary with unique global index as key
                        all_trajectories[global_idx] = {
                            'attempt_tokens': attempt_tokens,
                            'input_sequence': input_seq.tolist() if torch.is_tensor(input_seq) else input_seq,
                            'ground_truth': ground_truth_outputs[local_idx] if local_idx < len(ground_truth_outputs) else None,
                            'is_trajectory': True,
                            'source_file': file_path.name
                        }
                    
                    logging.info(f"Loaded {len(generated_sequences)} trajectories from {file_path} (old format)")
                    global_index_offset += len(generated_sequences)
                else:
                    logging.error(f"Unknown trajectory format in {file_path}: {type(data)}")
                    continue
                
            except Exception as e:
                logging.error(f"Error loading trajectory file {file_path}: {e}")
                continue
        
        self.trajectories = all_trajectories
        logging.info(f"Total trajectories loaded: {len(all_trajectories)} from {len(trajectory_files)} files")
        logging.info(f"Index range: 0 to {global_index_offset - 1}")
        
        return all_trajectories
    
    def _format_as_attempt_tokens(self, generated_output: Any) -> List[int]:
        """
        Format generated output as attempt tokens
        
        Args:
            generated_output: Generated sequence (output portion only)
            
        Returns:
            List of attempt tokens (without ATTEMPT_START/END markers)
        """
        # Convert to list if tensor
        if torch.is_tensor(generated_output):
            output_list = generated_output.tolist()
        else:
            output_list = generated_output
        
        # The attempt tokens are just the generated output
        # The ATTEMPT_START/END markers will be added by the dataset
        return output_list
    
    def format_for_training(self, sample_idx: int) -> Optional[Dict[str, Any]]:
        """
        Format a trajectory for training with attempt section
        
        Args:
            sample_idx: Index of the sample
            
        Returns:
            Dictionary with formatted training data or None if not found
        """
        if sample_idx not in self.trajectories:
            return None
        
        trajectory_data = self.trajectories[sample_idx]
        
        # Return the trajectory data
        # The dataset will handle inserting ATTEMPT_START/END
        return {
            'attempt_tokens': trajectory_data['attempt_tokens'],
            'ground_truth': trajectory_data.get('ground_truth'),
        }
    
    def get_trajectory_data(self) -> Dict[int, Dict[str, Any]]:
        """
        Get all loaded trajectory data
        
        Returns:
            Dictionary mapping sample indices to trajectory data
        """
        return self.trajectories
    
    def merge_with_dataset(self, dataset_size: int) -> Dict[int, Dict[str, Any]]:
        """
        Create trajectory data that can be merged with original dataset
        
        Args:
            dataset_size: Size of the original dataset
            
        Returns:
            Dictionary with trajectory data for indices that have trajectories
        """
        # Only return trajectories for indices within dataset size
        merged_data = {}
        for idx, traj_data in self.trajectories.items():
            if idx < dataset_size:
                merged_data[idx] = traj_data
        
        return merged_data


def main():
    """Test trajectory loading"""
    import argparse
    
    parser = argparse.ArgumentParser(description='Load and inspect trajectory files')
    parser.add_argument('--trajectory-folder', type=str, required=True,
                       help='Folder containing trajectory files')
    parser.add_argument('--sample-idx', type=int, default=0,
                       help='Sample index to inspect')
    
    args = parser.parse_args()
    
    # Setup logging
    logging.basicConfig(level=logging.INFO)
    
    # Load trajectories
    loader = TrajectoryLoader(args.trajectory_folder)
    trajectories = loader.load_trajectories()
    
    print(f"\nLoaded {len(trajectories)} trajectories")
    
    # Inspect specific sample
    if args.sample_idx in trajectories:
        traj = trajectories[args.sample_idx]
        print(f"\nTrajectory for sample {args.sample_idx}:")
        print(f"  Attempt tokens length: {len(traj['attempt_tokens'])}")
        if traj.get('ground_truth'):
            print(f"  Ground truth length: {len(traj['ground_truth'])}")
    else:
        print(f"\nNo trajectory found for sample {args.sample_idx}")
        print(f"Available indices: {sorted(trajectories.keys())[:10]}...")


if __name__ == "__main__":
    main()